def student_count(boys_count,girls_count)
  puts "This school has #{boys_count} boys."
  puts "This school has #{girls_count} girls."
  end

#type1
student_count(20,25)

#type2
number_of_boys = 10       #using variables
number_of_girls = 50

student_count(number_of_boys,number_of_girls)


#type3
student_count(10 + 50, 50 + 26)


#type4
student_count(number_of_boys + 100, number_of_girls + 200)
